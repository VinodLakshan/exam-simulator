    private void btCreateNewAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCreateNewAccountActionPerformed
        try {
            int res = checkFields();
            StudentController studentController = ServerConnector.getServerConnector().getStudentController();
            if (res == 0) {
                JOptionPane.showMessageDialog(this, "Passwords didn't Match....");
            } else if (res == 1) {
                JOptionPane.showMessageDialog(this, "All Fields must be Filled to Create Account....");
            } else if (res == 2) {
                JOptionPane.showMessageDialog(this, "Passwords didn't Match....");
            }else if (studentController.checkUserName(userName)) {
                JOptionPane.showMessageDialog(this, "User Name is Already Taken....");
            } else {
                Student student = new Student(stId, userName,stName, dob, gender, pass);
                boolean isWritten = studentController.writeStudent(student);

                if (isWritten) {
                    JOptionPane.showMessageDialog(this, "Account Creation Successfully....");
                    new MainForm(userName).setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Account Creation Failed....");
                }
            
            }
        } catch (IOException ex) {
            Logger.getLogger(CreateNewAccount.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(CreateNewAccount.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CreateNewAccount.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(CreateNewAccount.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btCreateNewAccountActionPerformed

