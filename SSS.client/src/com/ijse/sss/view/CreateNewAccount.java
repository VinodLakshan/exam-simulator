    private int checkFields() {
        stId = "st1";
        userName = txtUserName.getText();
        stName = txtName.getText();
        dob = txtDOB.getText();
        
        if (JrbMale.isSelected()) {
            gender = "Male";
        } else {
            gender = "Female";
        }
        pass = txtPass.getText();
        conPass = txtConPass.getText();

        if (userName.equals("") | stName.equals("") | pass.equals("") | conPass.equals("")) {
            if (!pass.equals(conPass)) {
                return 0;
            } else {
                return 1;
            }
        } else {
            if (!pass.equals(conPass)) {
                return 2;
            } else {
                return 3;
            }
        }
    }

