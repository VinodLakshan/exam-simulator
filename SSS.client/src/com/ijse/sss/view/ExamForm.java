    private void cbTypeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbTypeItemStateChanged
        String type = (String) cbType.getSelectedItem();
        ArrayList<Object[]> showExams = new ArrayList<>();
        DefaultTableModel defaultTableModel = (DefaultTableModel) jTable2.getModel();
        defaultTableModel.setRowCount(0);

        if (type.equalsIgnoreCase("All Exams")) {
            showExams = showExams("All Exams");
        } else if (type.equalsIgnoreCase("To be Held")) {
            showExams = showExams("To be Held");
        } else {
            showExams = showExams("Past Exams");
        }

        for (Object[] showExam : showExams) {
            defaultTableModel.addRow(showExam);
        }

    }//GEN-LAST:event_cbTypeItemStateChanged

