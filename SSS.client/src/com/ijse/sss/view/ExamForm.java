    private ArrayList<Object[]> showExams(String type) {
        ArrayList<Object[]> rowList = new ArrayList<>();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        if (type.equalsIgnoreCase("All Exams")) {
            for (Exam exam : allExam) {
                String modules = "";
                for (int i = 0; i < exam.getModules().length; i++) {
                    modules += exam.getModules()[i] + ",";
                }
                modules = modules + "\b";
                Object[] row = {exam.getEid(), exam.getExamName(), modules, exam.getDate(), exam.getTime(), exam.getDuration()};
                rowList.add(row);
            }
        } else if (type.equalsIgnoreCase("To be Held")) {
            try {
                Date curDate = dateFormat.parse(lblDate.getText());
                SimpleDateFormat sdf = new SimpleDateFormat("MMM dd,yyyy");
                for (Exam exam : allExam) {
                    Date examDate = sdf.parse(exam.getDate());
                    long diff = examDate.getTime() - curDate.getTime();
                    int diffDays = (int) (diff / (24 * 60 * 60 * 1000));

                    if (diffDays >= 0) {
                        String modules = "";
                        for (int i = 0; i < exam.getModules().length; i++) {
                            modules += exam.getModules()[i] + ",";
                        }
                        modules = modules + "\b";
                        Object[] row = {exam.getEid(), exam.getExamName(), modules, exam.getDate(), exam.getTime(), exam.getDuration()};
                        rowList.add(row);
                    }
                }
            } catch (ParseException ex) {
                Logger.getLogger(ExamForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                Date curDate = dateFormat.parse(lblDate.getText());
                SimpleDateFormat sdf = new SimpleDateFormat("MMM dd,yyyy");
                for (Exam exam : allExam) {
                    Date examDate = sdf.parse(exam.getDate());
                    long diff = examDate.getTime() - curDate.getTime();
                    int diffDays = (int) (diff / (24 * 60 * 60 * 1000));

                    if (diffDays < 0) {
                        String modules = "";
                        for (int i = 0; i < exam.getModules().length; i++) {
                            modules += exam.getModules()[i] + ",";
                        }
                        modules = modules + "\b";
                        Object[] row = {exam.getEid(), exam.getExamName(), modules, exam.getDate(), exam.getTime(), exam.getDuration()};
                        rowList.add(row);
                    }
                }
            } catch (ParseException ex) {
                Logger.getLogger(ExamForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        return rowList;
    }
