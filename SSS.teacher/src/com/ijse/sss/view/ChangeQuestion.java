    private void btBack1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBack1ActionPerformed
        int res = JOptionPane.showConfirmDialog(this, "Are You Sure You want to Change..??");
        if (res == 0) {
            String newQuestion = txtQuestion.getText();
            String newCorrectAnswer = txtCorrectAns.getText();
            String newAns1 = txtAns1.getText();
            String newAns2 = txtAns2.getText();
            String newAns3 = txtAns3.getText();
            String newModule = (String) cbModule.getSelectedItem();

            if (!newQuestion.equals("") && !newCorrectAnswer.equals("") && !newAns1.equals("") && !newAns2.equals("") && !newAns3.equals("")) {
                Question question = new Question(changingQid, newQuestion, newCorrectAnswer, newAns1, newAns2, newAns3, newModule);

                try {
                    QuestionController questionController = ServerConnector.getServerConnector().getQuestionController();
                    boolean isUpdate = questionController.updateQuestion(question);

                    if (isUpdate) {
                        JOptionPane.showMessageDialog(this, "Question Updated Successfully....");
                        questionController.releaseQuestion(this.changingQid);
                        new QuestionsForm(teacherMain).setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Question Updation Failed....");
                    }
                } catch (RemoteException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NotBoundException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                } catch (MalformedURLException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ParseException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please Fill all the Fields....");
            }
        }
    }//GEN-LAST:event_btBack1ActionPerformed

