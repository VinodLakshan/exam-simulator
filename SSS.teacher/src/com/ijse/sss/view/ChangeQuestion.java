    private void addQuestions(String module) {
        try {
            DefaultListModel<String> defaultListModel = new DefaultListModel<>();
            lstModule.setModel(defaultListModel);
            defaultListModel.removeAllElements();
            QuestionController questionController = ServerConnector.getServerConnector().getQuestionController();
            this.readQuestionsModule = questionController.readQuestionsModule(module);

            for (Question question : readQuestionsModule) {
                defaultListModel.addElement(question.getQuestion());
            }

        } catch (RemoteException ex) {
            Logger.getLogger(ChangeNotes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException | MalformedURLException ex) {
            Logger.getLogger(ChangeNotes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ChangeNotes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void previewQuestion() {
        String selectedValue = lstModule.getSelectedValue();
        String changingQid;
        for (Question question : readQuestionsModule) {
            if (selectedValue.equals(question.getQuestion())) {
                try {
                    QuestionController questionController = ServerConnector.getServerConnector().getQuestionController();
                    changingQid = question.getQid();

                    if (this.changingQid != changingQid) {
                        questionController.releaseQuestion(this.changingQid);
                    }
                    this.changingQid = changingQid;

                    if (questionController.reserveQuestion(changingQid)) {
                        txtQuestion.setText(question.getQuestion());
                        txtCorrectAns.setText(question.getCorrectAnswer());
                        txtAns1.setText(question.getAnswer1());
                        txtAns2.setText(question.getAnswer2());
                        txtAns3.setText(question.getAnswer3());
                        cbModule.setSelectedItem(question.getModule());
                        btBack1.setEnabled(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "This Question is already Reserved....");
                    }

                } catch (RemoteException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                } catch (NotBoundException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                } catch (MalformedURLException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(ChangeQuestion.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

